package ex1;

public class Warrior1 {
	// hp , power , name 이것들 getter setter로 만들어보기 getter setter연습많이하기 많이쓰인다.
	private int hp;
	private int power;
	private String name;
	private String skill;
	private int level;
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getPower() {
		return power;
	}
	public void setPower(int power) {
		this.power = power;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSkill() {
		return skill;
	}
	public void setSkill(String skill) {
		this.skill = skill;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	//getter setter메서드 단축키
	//source - generate getters and setters // 방어적 코드는 추가해야함



	
}
